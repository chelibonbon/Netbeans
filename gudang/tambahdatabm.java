    class brg_masuk extends tambahdatabm{
            int id_bm, id_brg, jumlah;
            String tanggal;
            
            public brg_masuk() {
                String combo = boxidbarang.getSelectedItem().toString();
                String[] arr = combo.split(":");
                this.id_brg = Integer.parseInt(arr[0]);
                try {
                    Date date = texttanggalbm.getDate();
                    DateFormat dateformat = new SimpleDateFormat ("YYYY-MM-dd");
                    this.tanggal = dateformat.format(date);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
                this.jumlah = Integer.parseInt(textjumlahbm.getText());
            }
        }
    
        public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("id bm");
        model.addColumn("id brg");
        model.addColumn("jumlah");
        model.addColumn("tanggal");
        tablebm.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from brg_masuk ");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                model.addRow(data); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textidbm.setText("");
        textjumlahbm.setText("");
    }
        public void refreshCombo(){
            try {
                    this.stat = k.getCon().prepareStatement("select * from barang ");
                    this.rs = this.stat.executeQuery();
                   while (rs.next()) {
                       boxidbarang.addItem(rs.getString("id_brg")+":"+
                       rs.getString("nama_barang")+":"+rs.getString("kode_barang")+":"+rs.getString("stok"));
                   }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

