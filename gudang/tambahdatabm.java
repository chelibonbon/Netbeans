    private void tombolinputbmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolinputbmActionPerformed
        // TODO add your handling code here:
         try {
            brg_masuk barm = new brg_masuk();
            this.stat = k.getCon().prepareStatement("Insert into brg_masuk values(?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setInt(2, barm.id_brg);
            this.stat.setInt(3, barm.jumlah);
            this.stat.setString(4, barm.tanggal);
            this.stat.executeUpdate();
            refreshTable();
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolinputbmActionPerformed

    private void tombolupdatebmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolupdatebmActionPerformed
        // TODO add your handling code here:
          try {
            brg_masuk barm = new brg_masuk();
            this.stat =  k.getCon().prepareStatement("update brg_masuk set id_brg=?,"
                    + "jumlah=?,tanggal=?" + "where id_bm=?");
            stat.setInt(1, barm.id_brg);
            stat.setInt(2, barm.jumlah);
            stat.setString(3, barm.tanggal);
            stat.setString(4, textidbm.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolupdatebmActionPerformed

