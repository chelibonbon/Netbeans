    private void tombolinputbkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolinputbkActionPerformed
        // TODO add your handling code here:
        try {
            brg_keluar bark = new brg_keluar();
            this.stat = k.getCon().prepareStatement("Insert into brg_keluar values(?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setInt(2, bark.id_bm);
            this.stat.setInt(3, bark.jumlah);
            this.stat.setString(4, bark.tanggal);
            this.stat.executeUpdate();
            refreshTable();
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolinputbkActionPerformed

    private void tombolupdatebkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolupdatebkActionPerformed
        // TODO add your handling code here:
        try {
            brg_keluar bark = new brg_keluar();
            this.stat =  k.getCon().prepareStatement("update brg_keluar set id_bm=?,"
                    + "jumlah=?,tanggal=? where id_bk=?");
            stat.setInt(1, bark.id_bm);
            stat.setInt(2, bark.jumlah);
            stat.setString(3, bark.tanggal);
            stat.setString(4, textidbk.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolupdatebkActionPerformed

