    class brg_keluar extends tambahdatabk{
            int id_bk, id_bm, jumlah;
            String tanggal;
            
            public brg_keluar() {
                String combo = boxidbm.getSelectedItem().toString();
                String[] arr = combo.split(":");
                this.id_bm = Integer.parseInt(arr[0]);
                try {
                    Date date = texttanggalbk.getDate();
                    DateFormat dateformat = new SimpleDateFormat ("YYYY-MM-dd");
                    this.tanggal = dateformat.format(date);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
                this.jumlah = Integer.parseInt(textjumlahbk.getText());
            }
        }
    
        public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("id bk");
        model.addColumn("id bm");
        model.addColumn("jumlah");
        model.addColumn("tanggal");
        tablebk.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from brg_keluar ");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                model.addRow(data); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textidbk.setText("");
        textjumlahbk.setText("");
    }
        public void refreshCombo(){
            try {
                    this.stat = k.getCon().prepareStatement("select * from brg_masuk ");
                    this.rs = this.stat.executeQuery();
                   while (rs.next()) {
                       boxidbm.addItem(rs.getString("id_bm")+":"+
                       rs.getString("id_brg")+":"+rs.getString("jumlah")+":"+rs.getString("tanggal"));
                   }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
