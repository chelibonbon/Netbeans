    class brg_rusak extends tambahdatabr{
            int id_br, id_bk, jumlah;
            String tanggal;
            
            public brg_rusak() {
                String combo = boxidbk.getSelectedItem().toString();
                String[] arr = combo.split(":");
                this.id_bk = Integer.parseInt(arr[0]);
                try {
                    Date date = texttanggalbr.getDate();
                    DateFormat dateformat = new SimpleDateFormat ("YYYY-MM-dd");
                    this.tanggal = dateformat.format(date);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
                this.jumlah = Integer.parseInt(textjumlahbr.getText());
            }
        }
    
        public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("id br");
        model.addColumn("id bk");
        model.addColumn("jumlah");
        model.addColumn("tanggal");
        tablebr.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from brg_rusak ");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                };
                model.addRow(data); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textidbr.setText("");
        textjumlahbr.setText("");
    }
        public void refreshCombo(){
            try {
                    this.stat = k.getCon().prepareStatement("select * from brg_keluar ");
                    this.rs = this.stat.executeQuery();
                   while (rs.next()) {
                       boxidbk.addItem(rs.getString("id_bk")+":"+
                       rs.getString("id_bm")+":"+rs.getString("jumlah")+":"+rs.getString("tanggal"));
                   }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

