    private void tombolinputbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolinputbrActionPerformed
        // TODO add your handling code here:
         try {
            brg_rusak barr = new brg_rusak();
            this.stat = k.getCon().prepareStatement("Insert into brg_rusak values(?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setInt(2, barr.id_bk);
            this.stat.setInt(3, barr.jumlah);
            this.stat.setString(4, barr.tanggal);
            this.stat.executeUpdate();
            refreshTable();
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolinputbrActionPerformed

    private void tombolupdatebrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolupdatebrActionPerformed
        // TODO add your handling code here:
         try {
            brg_rusak barr = new brg_rusak();
            this.stat =  k.getCon().prepareStatement("update brg_rusak set id_bk=?,"
                    + "jumlah=?,tanggal=?" + "where id_br=?");
            stat.setInt(1, barr.id_bk);
            stat.setInt(2, barr.jumlah);
            stat.setString(3, barr.tanggal);
            stat.setString(4, textidbr.getText());
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolupdatebrActionPerformed

